package picoded.core.common;

import org.apache.commons.lang3.ArrayUtils;

/**
 * Provides a standardised access to empty arrays
 *
 * This proxies the object found in ArrayUtils (from apache)
 **/
public class EmptyArray {
	
	/**
	 * An empty immutable string array
	 **/
	public static final String[] STRING = ArrayUtils.EMPTY_STRING_ARRAY;
	/**
	 * An empty immutable boolean array.
	 **/
	public static final boolean[] BOOLEAN = ArrayUtils.EMPTY_BOOLEAN_ARRAY;
	/**
	 * An empty immutable Boolean object array.
	 **/
	public static final Boolean[] BOOLEAN_OBJECT = ArrayUtils.EMPTY_BOOLEAN_OBJECT_ARRAY;
	/**
	 * An empty immutable byte array.
	 **/
	public static final byte[] BYTE = ArrayUtils.EMPTY_BYTE_ARRAY;
	/**
	 * An empty immutable Byte array.**
	 **/
	public static final Byte[] BYTE_OBJECT = ArrayUtils.EMPTY_BYTE_OBJECT_ARRAY;
	/**
	 * An empty immutable char array.
	 **/
	public static final char[] CHAR = ArrayUtils.EMPTY_CHAR_ARRAY;
	/**
	 * An empty immutable Character array.
	 **/
	public static final Character[] CHARACTER = ArrayUtils.EMPTY_CHARACTER_OBJECT_ARRAY;
	/**
	 * An empty immutable Class array.
	 **/
	public static final Class<?>[] CLASS = ArrayUtils.EMPTY_CLASS_ARRAY;
	/**
	 * An empty immutable double array.
	 **/
	public static final double[] DOUBLE = ArrayUtils.EMPTY_DOUBLE_ARRAY;
	/**
	 * An empty immutable Double array.
	 **/
	public static final Double[] DOUBLE_OBJECT = ArrayUtils.EMPTY_DOUBLE_OBJECT_ARRAY;
	/**
	 * An empty immutable float array.
	 **/
	public static final float[] FLOAT = ArrayUtils.EMPTY_FLOAT_ARRAY;
	/**
	 * An empty immutable Float array.
	 **/
	public static final Float[] FLOAT_OBJECT = ArrayUtils.EMPTY_FLOAT_OBJECT_ARRAY;
	/**
	 * An empty immutable int array.
	 **/
	public static final int[] INT = ArrayUtils.EMPTY_INT_ARRAY;
	/**
	 * An empty immutable Integer array.
	 **/
	public static final Integer[] INT_OBJECT = ArrayUtils.EMPTY_INTEGER_OBJECT_ARRAY;
	/**
	 * An empty immutable long array.
	 **/
	public static final long[] LONG = ArrayUtils.EMPTY_LONG_ARRAY;
	/**
	 * An empty immutable Long array.
	 **/
	public static final Long[] LONG_OBJECT = ArrayUtils.EMPTY_LONG_OBJECT_ARRAY;
	/**
	 * An empty immutable Object array.
	 **/
	public static final Object[] OBJECT = ArrayUtils.EMPTY_OBJECT_ARRAY;
	/**
	 * An empty immutable short array.
	 **/
	public static final short[] SHORT = ArrayUtils.EMPTY_SHORT_ARRAY;
	/**
	 * An empty immutable Short array.
	 **/
	public static final Short[] SHORT_OBJECT = ArrayUtils.EMPTY_SHORT_OBJECT_ARRAY;
	/**
	 * The index value when an element is not found in a list or array: -1.
	 **/
	public static final int INDEX_NOT_FOUND = ArrayUtils.INDEX_NOT_FOUND;
	
}
